public class Main {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++)//loop for counting rows
        {
            for (int j = 0; j < 5; j++)//loop for counting columns
            {
                System.out.print("*");//loop for printing out stars
            }
            System.out.println();//loops for printing a new line
        }
    }
}